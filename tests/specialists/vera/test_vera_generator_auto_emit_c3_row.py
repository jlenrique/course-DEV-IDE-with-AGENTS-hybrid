def _read_pyproject(repo_root: Path) -> str:
    return (repo_root / "pyproject.toml").read_text(encoding="utf-8")


def _c3_ignore_imports(pyproject_text: str) -> list[str]:
    marker = "ignore_imports = ["
    start = pyproject_text.index(marker) + len(marker)
    end = pyproject_text.index("]", start)
    body = pyproject_text[start:end]
    values: list[str] = []
    for line in body.splitlines():
        line = line.strip()
        if not line or line.startswith("#"):
            continue
        line = line.split("#", 1)[0].strip().rstrip(",")
        if line:
            values.append(ast.literal_eval(line))
    return values


def test_temp_pyproject_baseline_is_5_rows(temp_repo_root: Path) -> None:
    pyproject = _read_pyproject(temp_repo_root)
    actual = _c3_ignore_imports(pyproject)
    baseline = _c3_ignore_imports(TEMP_PYPROJECT)
    assert len(actual) == 5
    assert set(actual) == set(baseline)


