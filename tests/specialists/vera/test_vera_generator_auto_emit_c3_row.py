def test_post_emit_temp_pyproject_has_vera_row_and_no_duplicate(
    temp_repo_root: Path, make_request
) -> None:
    vera_skill = temp_repo_root / "skills" / "bmad-agent-fidelity-assessor"
    vera_skill.mkdir(parents=True, exist_ok=True)
    (vera_skill / "SKILL.md").write_text("# vera skill", encoding="utf-8")

    generate.generate_specialist(
        make_request(
            name="vera",
            mcp_tool="none",
            expertise_tier="L4-fidelity-assessment",
            from_skill=vera_skill,
            repo_root=temp_repo_root,
            force=True,
        )
    )
    pyproject = _read_pyproject(temp_repo_root)
    target = "app.specialists.vera.graph -> app.gates.resume_api"
    rows = _c3_ignore_imports(pyproject)
    assert target in rows
    assert rows.count(target) == 1
    assert "# generated by bmad-create-specialist for vera" in pyproject
