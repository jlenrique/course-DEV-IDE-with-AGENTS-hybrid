# ---------------------------------------------------------------------------
# AC-2 (end-to-end) — PAUSED gates route to the bespoke renderers via trial.py.
# ---------------------------------------------------------------------------


def _write_paused_run(
    tmp_path: Path, *, gate: str, card_body: dict[str, Any]
) -> dict[str, Any]:
    """Materialize a minimal paused-at-gate run dir + payload for the given gate."""
    run_dir = tmp_path / "run"
    run_dir.mkdir(parents=True, exist_ok=True)
    (run_dir / f"decision-card-{gate}.json").write_text(
        json.dumps({"card": card_body}, indent=2), encoding="utf-8"
    )
    return {
        "status": "paused-at-gate",
        "trial_id": "aaaaaaaa-bbbb-4ccc-8ddd-eeeeeeeeeeee",
        "paused_gate": gate,
        "operator_id": "operator",
        "run_registry_path": str(run_dir / "run-registry.json"),
    }


def test_paused_motion_plan_routes_to_bespoke_renderer_via_trial(
    tmp_path: Path, capsys: pytest.CaptureFixture[str]
) -> None:
    """AC-2 (end-to-end): a paused motion-plan gate keyed by its section_07d ``surface_id``
    (the sole routing key; the shared "G2C" fold-target is deliberately unmapped) reaches the
    BESPOKE motion-plan renderer, NOT the generic fallback."""
    card_body = _load(MOTION_PLAN_FIXTURE)
    payload = _write_paused_run(
        tmp_path, gate="section_07d_g2_5_motion_plan_polling", card_body=card_body
    )

    trial._emit_gate_surface_if_paused(payload, runs_root=tmp_path)

    err = capsys.readouterr().err
    assert "G2.5 motion-plan status" in err  # bespoke renderer fired
    assert "11111111-1111-4111-8111-111111111111" in err
    assert "| Field | Value |" not in err  # generic fallback did NOT fire


